                    <div class="body-text black-text-color text-left mt-5 px-5">
                        Jika kita biasanya hanya memadukan roti dengan mentega lalu ditaburi gula, kini ada satu cara yang lebih seru untuk teman kamu bersantai di rumah. Resep kali ini gurih, harum, dan cocok disantap setiap saat. Roti sobek smoked beef bisa menjadi pilihan saat menghabiskan waktu bersama teman dan keluarga.

                        Konsep dari roti sobek smoked beef sendiri mirip dengan jenis lainnya. Namun, alih-alih menggunakan isian manis seperti cokelat, keju, ataupun selai, resep ini memadukan kelembutan roti dengan topping gurih berupa smoked beef, mayones, serta tak lupa Saus Sambal Jawara Hot yang nendang di mulut dan bikin ingin nambah terus. Cara membuatnya pun mudah, cukup dengan bahan-bahan yang tersedia di dapur saja.

                        Tersaji hangat dari bara oven, padukan roti sobek smoked beef dengan secangkir teh atau kopi. Maka, lengkap sudah momen istimewa bersama orang-orang tersayang! Penasaran dengan resep roti lain yang tak kalah lezatnya dengan yang satu ini? Cobain resep unik lainnya seperti roti jala kuah kari, roti canai, ataupun Korean Garlic Bread yang tak kalah menggoda selera!

                        Tunggu apa lagi? Yuk, kita berangkat ke dapur!<br><br>


                        bahan-bahan<br>
                        <br>
                        2 kuning telur ayam<br>
                        50g mentega tawar<br>
                        150ml susu cair hangat<br>
                        400g tepung terigu protein tinggi<br>
                        3sdm susu bubuk<br>
                        1sdt garam<br>
                        100g gula pasir<br>
                        1.5sdt ragi instan<br>
                        <br>
                        olesan<br><br>
                        1 kuning telur ayam, campur dengan 2 sdm susu cair<br>
                        1sdm peterseli<br>

                        isi<br><br>
                        12 lembar daging Asap<br>
                        4 sdm jawara saus sambal Hot<br>
                        2 sdm hellman's Real Mayonnaise<br>
                        50g keju cheddar, parut<br><br>


                        Cara membuat<br><br>
                        1. Olesi loyang ukuran 15x10x4 cm dengan mentega hingga rata. Panaskan oven pada suhu 200° C. Sisihkan. Campur bahan kering dengan telur, aduk rata. Tuang susu cair sedikit demi sedikit, sambil diuleni hingga tercampur rata. Tambahkan mentega, uleni kembali hingga kalis.<br>
                        2. Letakkan adonan di dalam mangkuk, tutup dengan plastik. Sisihkan hingga mengembang 2 kali (± 30 menit).<br>
                        3. Kempiskan adonan, gilas menggunakan rolling pin. Susun melebar smoked beef ke atasnya. Olesi dengan mayones dan Saus Sambal Jawara Hot. Tambahkan keju cheddar parut. Gulung roti. Potong-potong setebal 3 cm.<br>
                        4. Siapkan loyang kotak yang sudah diolesi mentega secara merata. Tata potongan roti ke atasnya. Diamkan hingga mengembang (± 15 menit).<br>
                        5. Olesi permukaan roti dengan kuning telur secara merata. Taburi dengan peterseli. Panggang di dalam oven panas hingga matang (± 20 menit), keluarkan. Olesi permukaannya dengan mentega. Sajikan.<br>
